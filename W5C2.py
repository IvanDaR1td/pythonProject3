# THE BIO FUNCTION
def bio(name, born, pronoun):
        print(name.capitalize(), "was born in", born)
        age = 2021 - born
        print(pronoun.capitalize(), "will turn", age, "years old this year")
        future = born + 67
        print(pronoun.capitalize(), "will be 67 in the year", future)

# PRINT BIOS
bio("stephen",1984,"he")
bio("mary",1990,"she")
bio("jane",2000,"she")

